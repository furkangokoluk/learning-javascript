// new Date()                   : Şu anki tarih ve saati temsil eden bir Date nesnesi oluşturur.
// Date.now()                   : Epoch'tan (1970-01-01T00:00:00Z) itibaren geçen milisaniye cinsinden zamanı verir.
// dateObj.getFullYear()        : Yılın dört haneli bir sayı olarak değerini döndürür.
// dateObj.getMonth()           : Ayı, 0'dan başlayarak döndürür (0 = Ocak, 1 = Şubat, ..., 11 = Aralık).
// dateObj.getDate()            : Ayın gününü döndürür (1 ile 31 arasında bir değer).
// dateObj.getDay()             : Haftanın gününü, 0'dan (Pazar) 6'ya (Cumartesi) döndürür.
// dateObj.getHours()           : Saati döndürür (0 ile 23 arasında bir değer).
// dateObj.getMinutes()         : Dakikayı döndürür (0 ile 59 arasında bir değer).
// dateObj.getSeconds()         : Saniyeyi döndürür (0 ile 59 arasında bir değer).
// dateObj.getMilliseconds()    : Milisaniyeyi döndürür (0 ile 999 arasında bir değer).
// dateObj.getTime()            : Unix zaman damgasını, yani 1970'ten bu yana geçen milisaniye sayısını döndürür.
// dateObj.setTime(milisaniye)  : Date nesnesinin zamanını belirtilen Unix zaman damgasına ayarlar.
// dateObj.toLocaleDateString() : Date nesnesinin tarih kısmını yerelleştirilmiş bir şekilde döndürür.
// dateObj.toLocaleTimeString() : Date nesnesinin saat kısmını yerelleştirilmiş bir şekilde döndürür.
// dateObj.toLocaleString()     : Date nesnesinin tarihi ve saat kısmını yerelleştirilmiş bir şekilde döndürür.